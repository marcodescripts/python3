# Operaciones aritméticas ( Al final del video cambiamos los voalores
#  numeroUno y de numeroDos para ejemplificar mejor el módulo )

numeroUno = 95
numeroDos = 45

# suma
numeroTres = numeroUno + numeroDos
print(numeroTres)

# Resta
numeroTres = numeroUno - numeroDos
print(numeroTres)
 
 # Multiplicación
numeroTres = numeroUno * numeroDos
print(numeroTres)

# División
numeroTres = numeroUno / numeroDos
print(numeroTres)
 
# Módulo
numeroTres = numeroUno % numeroDos
print(numeroTres)
 
# Potencia
numeroTres = numeroUno ** numeroDos
print(numeroTres)



